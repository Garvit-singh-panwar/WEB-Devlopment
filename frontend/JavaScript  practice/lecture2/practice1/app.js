let color = "green";

// switch case
switch (color){
case "red":
    console.log("Stop");
    break;
case "green":
    console.log("go");
    break;
case "yellow":
    console.log("slow down");
    break;
};

// else if statement 
if(color === "red"){
    console.log("stop");
}
else if(color === "green")
{
    console.log("go");
}
else if(color === "yellow")
{
    console.log("slowdown");
}